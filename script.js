// Обработка хэша URL при загрузке
function handleHashChange() {
  const hash = window.location.hash.substring(1);
  if (!hash) {
    setActiveSection('home');
    return;
  }

  const params = new URLSearchParams(hash);
  const city = params.get('city');
  const category = params.get('category');

  if (city && category) {
    showCategoryContent(city, decodeURIComponent(category));
  } else if (city) {
    showCityContent(city);
  } else if (hash === 'catalog') {
    setActiveSection('catalog');
  } else if (hash === 'support') {
    setActiveSection('support');
  } else {
    setActiveSection('home');
  }
}

// Обработчик изменения хэша
window.addEventListener('hashchange', handleHashChange);

// При загрузке показываем соответствующую страницу
window.addEventListener('DOMContentLoaded', () => {
  handleHashChange();

  // Если нет хэша, показываем главную
  if (!window.location.hash) {
    setActiveSection('home');
  }
});

