// Функция для расшаривания места
function sharePlace(city, category, placeName) {
  const shareText = `Посмотрите это место в ${city}: ${placeName} (${category})`;
  const shareUrl = window.location.href;

  if (navigator.share) {
    navigator.share({
      title: 'LEISURE & FUN - Рекомендация',
      text: shareText,
      url: shareUrl
    }).then(() => {
      showNotification('Спасибо за рекомендацию!', 'success');
    }).catch(err => {
      console.log('Ошибка шаринга:', err);
      copyToClipboard(shareText + '\n' + shareUrl, null);
    });
  } else {
    copyToClipboard(shareText + '\n' + shareUrl, null);
  }
}

